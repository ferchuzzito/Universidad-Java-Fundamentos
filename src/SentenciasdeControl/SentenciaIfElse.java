package SentenciasdeControl;

public class SentenciaIfElse {

	public static void main(String[] args) {
		var numero = 2;
		if(numero == 1) {
			System.out.println("numero 1");
			}
		else if (numero == 2) {
			System.out.println("numero 2");
			}
		else {
			System.out.println("numero desconocido");
		}
	}

}
