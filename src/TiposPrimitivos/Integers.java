package TiposPrimitivos;

public class Integers {

	public static void main(String[] args) {
		//Variable de tipo byte
		System.out.println("bits de tipo Byte: " + Byte.SIZE);
		System.out.println("bytes de tipo Byte: " + Byte.BYTES);
		System.out.println("valor minimo de tipo Byte: " + Byte.MIN_VALUE);
		System.out.println("valor minimo de tipo Byte: " + Byte.MAX_VALUE);
		//Variable de tipo short
		System.out.println();
		System.out.println("bits de tipo Short: " + Short.SIZE);
		System.out.println("bytes de tipo Short: " + Short.BYTES);
		System.out.println("valor minimo de tipo short: " + Short.MIN_VALUE);
		System.out.println("valor minimo de tipo short: " + Short.MAX_VALUE);
		//Variable de tipo int
		System.out.println();
		System.out.println("bits de tipo Integer: " + Integer.SIZE);
		System.out.println("bytes de tipo Integer: " + Integer.BYTES);
		System.out.println("valor minimo de tipo Integer: " + Integer.MIN_VALUE);
		System.out.println("valor minimo de tipo Integer: " + Integer.MAX_VALUE);
		//Variable de tipo long
		System.out.println();
		System.out.println("bits de tipo Long: " + Long.SIZE);
		System.out.println("bytes de tipo Long: " + Long.BYTES);
		System.out.println("valor minimo de tipo Long: " + Long.MIN_VALUE);
		System.out.println("valor minimo de tipo Long: " + Long.MAX_VALUE);
	}

}
