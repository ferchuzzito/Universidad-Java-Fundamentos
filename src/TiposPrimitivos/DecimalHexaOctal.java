package TiposPrimitivos;

public class DecimalHexaOctal {

	public static void main(String[] args) {
		//valor 10 en Decimal
		var b1 = 10;
		System.out.println("b1 = " +b1);
		//valor 10 en Hexadecimal
		var b2 = 0xa;
		System.out.println("b2 = " +b2);
		//valor 10 en Octal
		var b3 = 012;
		System.out.println("b3 = " +b3);
	}

}
