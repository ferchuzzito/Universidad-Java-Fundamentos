package TiposPrimitivos;

public class Flotantes {

	public static void main(String[] args) {
		//Variable de tipo Float
		System.out.println("bits de tipo Float: " + Float.SIZE);
		System.out.println("bytes de tipo Float: " + Float.BYTES);
		System.out.println("valor minimo de tipo Float: " + Float.MIN_VALUE);
		System.out.println("valor minimo de tipo Float: " + Float.MAX_VALUE);
		//Variable de tipo Double
		System.out.println();
		System.out.println("bits de tipo Double: " + Double.SIZE);
		System.out.println("bytes de tipo Double: " + Double.BYTES);
		System.out.println("valor minimo de tipo Double: " + Double.MIN_VALUE);
		System.out.println("valor minimo de tipo Double: " + Double.MAX_VALUE);
	}

}
